#include <stddef.h>
#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*p;

	p = s;
	i = -1;
	while (++i < n)
		p[i] = c;
	return (s);
}
